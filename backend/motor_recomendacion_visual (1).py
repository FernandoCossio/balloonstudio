"""---
## Sección 6: Almacén de embeddings en memoria

En el prototipo de Colab **no conectamos a PostgreSQL**. En cambio, usamos un almacén en memoria que replica exactamente el comportamiento de pgvector.

**Por qué en memoria y no en PostgreSQL aquí:**
- El objetivo de esta fase es validar la calidad de las recomendaciones, no la infraestructura.
- La lógica de búsqueda por similitud coseno es idéntica — solo cambia dónde se almacenan los vectores.
- Conectar a PostgreSQL desde Colab requeriría exponer la BD o tunelizar la conexión, añadiendo complejidad que no aporta a la validación.

La clase `EmbeddingStore` imita exactamente la query pgvector:
```sql
SELECT id, nombre, 1 - (embedding <=> $query) AS score
FROM articulo_inventario
ORDER BY embedding <=> $query
LIMIT $top_k;
```
"""

# ==============================================================================
# SECCIÓN 6 — Almacén de embeddings en memoria (replica pgvector)
# ==============================================================================

import numpy as np
from dataclasses import dataclass, field
from typing import Any


@dataclass
class ArticuloIndexado:
    """Representa un artículo del inventario con su embedding generado."""
    id: str
    nombre: str
    imagen_path: str
    embedding: np.ndarray
    metadata: dict[str, Any] = field(default_factory=dict)


class EmbeddingStore:
    """
    Almacén en memoria de embeddings del inventario.

    Replica el comportamiento del operador <=> de pgvector:
        1 - distancia_coseno(a, b) == producto_punto(a, b)  [vectores normalizados]

    En producción este almacén no existe: los embeddings viven en
    ArticuloInventario.embedding_visual (PostgreSQL vector(512)) y la
    búsqueda la ejecuta pgvector directamente en SQL.
    """

    def __init__(self):
        self._articulos: list[ArticuloIndexado] = []

    def agregar(self, articulo: ArticuloIndexado) -> None:
        """Agrega o reemplaza un artículo en el almacén."""
        # Upsert: si ya existe el ID, reemplaza
        self._articulos = [a for a in self._articulos if a.id != articulo.id]
        self._articulos.append(articulo)

    def buscar(self, query_embedding: list[float], top_k: int = 5, filtro_tipo: str | None = None) -> list[dict]:
        """
        Busca los artículos más similares al query_embedding.

        Equivale a:
            SELECT *, 1 - (embedding <=> $query) AS score
            FROM articulo_inventario
            ORDER BY embedding <=> $query
            LIMIT $top_k;

        Args:
            query_embedding: vector de 512 dimensiones normalizado L2.
            top_k: número de resultados a retornar.

        Returns:
            Lista de dicts con id, nombre, score y metadata, ordenados
            por score descendente (mayor similitud primero).
        """

        # Filtrado previo de los artículos si se especifica el tipo
        articulos_candidatos = self._articulos
        if filtro_tipo:
            articulos_candidatos = [
                a for a in articulos_candidatos
                if a.metadata.get("tipo") == filtro_tipo or a.metadata.get("tipo_articulo") == filtro_tipo
            ]

        if not articulos_candidatos:
            return []

        # Convertir query a numpy array
        query_vec = np.array(query_embedding, dtype=np.float32)

        # Construir matriz solo con los candidatos filtrados
        matriz = np.vstack([a.embedding for a in articulos_candidatos])

        # Multiplicación matricial (Similitud coseno debido a la normalización L2)
        scores = matriz @ query_vec

        # Ordenar índices descendentemente
        indices_ordenados = np.argsort(scores)[::-1][:top_k]

        resultados = []
        for idx in indices_ordenados:
            articulo = articulos_candidatos[idx]
            resultados.append({
                "id": articulo.id,
                "nombre": articulo.nombre,
                "score": float(scores[idx]),
                "metadata": articulo.metadata
            })

        return resultados

    @property
    def total_indexados(self) -> int:
        return len(self._articulos)


