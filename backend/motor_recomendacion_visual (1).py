"""---
## Sección 4: CLIPService — Wrapper del modelo

Esta clase es el núcleo del sistema. Encapsula toda la lógica de interacción con los modelos CLIP.

**Decisiones de implementación:**
- Se llaman `get_image_features()` y `get_text_features()` directamente, **nunca** `forward()`. El método `forward()` computa pérdidas de entrenamiento que no se necesitan en inferencia.
- La **normalización L2** es obligatoria antes de retornar cualquier vector. Sin ella, el producto punto entre vectores no es equivalente a similitud coseno.
- `torch.no_grad()` desactiva el cálculo del grafo de gradientes. Reduce el uso de memoria a la mitad en inferencia.
- Los dos modelos se cargan una sola vez al instanciar la clase.
"""

# ==============================================================================
# SECCIÓN 4 — CLIPService: wrapper del modelo
# ==============================================================================

import torch
import torch.nn.functional as F
import numpy as np
from PIL import Image
from pathlib import Path
from transformers import CLIPModel, CLIPProcessor
from sentence_transformers import SentenceTransformer
import io


class CLIPService:
    """
    Wrapper de los modelos CLIP para generación de embeddings.

    Modelos:
        - Imagen: openai/clip-vit-base-patch32
        - Texto (español): sentence-transformers/clip-ViT-B-32-multilingual-v1

    Ambos producen vectores de 512 dimensiones en el mismo espacio vectorial,
    lo que permite comparar embeddings de imagen contra embeddings de texto
    usando similitud coseno.
    """

    MODELO_IMAGEN_ID = "openai/clip-vit-base-patch32"
    MODELO_TEXTO_ID  = "sentence-transformers/clip-ViT-B-32-multilingual-v1"
    DIMENSION        = 512

    def __init__(self, device: str = None):
        self.device = device or ("cuda" if torch.cuda.is_available() else "cpu")
        print(f"[CLIPService] Iniciando en dispositivo: {self.device}")

        # --- Encoder de imagen ---
        # Se usa CLIPModel completo pero solo se llama get_image_features() en inferencia.
        print(f"[CLIPService] Cargando encoder de imagen ({self.MODELO_IMAGEN_ID})...")
        self._modelo_imagen    = CLIPModel.from_pretrained(self.MODELO_IMAGEN_ID).to(self.device)
        self._procesador_imagen = CLIPProcessor.from_pretrained(self.MODELO_IMAGEN_ID)
        self._modelo_imagen.eval()  # desactiva dropout y batch norm de entrenamiento

        # --- Encoder de texto multilingüe ---
        # SentenceTransformer maneja internamente la tokenización y la normalización.
        # Produce embeddings compatibles con el espacio del modelo de imagen base.
        print(f"[CLIPService] Cargando encoder de texto ({self.MODELO_TEXTO_ID})...")
        self._modelo_texto = SentenceTransformer(self.MODELO_TEXTO_ID, device=self.device)

        print(f"[CLIPService] ✅ Modelos cargados. Dimensión de embeddings: {self.DIMENSION}")

    def encode_image(self, fuente: str | Path | bytes) -> list[float]:
        """
        Genera el embedding de una imagen de forma robusta extrayendo la proyección final.
        """
        imagen = self._cargar_imagen(fuente)

        inputs = self._procesador_imagen(
            images=imagen,
            return_tensors="pt"
        ).to(self.device)

        with torch.no_grad():
            # get_image_features nos devuelve el tensor ya proyectado al espacio de 512
            outputs = self._modelo_imagen.get_image_features(**inputs)

            # Si por alguna razón la versión de transformers devuelve el contenedor BaseModelOutputWithPooling,
            # extraemos el 'pooler_output' o el primer elemento, que es el vector global de la imagen.
            if hasattr(outputs, "pooler_output") and outputs.pooler_output is not None:
                features = outputs.pooler_output
            elif isinstance(outputs, torch.Tensor):
                features = outputs
            else:
                features = outputs[0]

        # Nos aseguramos de tener un shape de [1, 512] antes de normalizar
        if features.dim() > 2:
            # Si viene con secuencia (como el 38400 anterior), tomamos solo el token CLS (índice 0)
            features = features[:, 0, :]

        # Normalización L2 sobre las 512 dimensiones
        features = F.normalize(features, p=2, dim=-1)

        # Retornamos el vector plano de 512 elementos
        return features.view(-1).cpu().tolist()

    QUERY_TEMPLATE = (
        "decoración de eventos: {prompt}. "
        "Estilo visual, colores, materiales y ambiente."
    )

    def encode_text(self, texto: str) -> list[float]:
        """
        Genera el embedding de un texto en español.

        Args:
            texto: prompt del cliente en español.

        Returns:
            Vector normalizado L2 de 512 dimensiones como list[float].

        Raises:
            ValueError: si el texto está vacío.
        """

        texto = texto.strip()
        if not texto:
            raise ValueError("El texto no puede estar vacío.")

        # Enriquecer el prompt aplicando el template de expansión
        texto_expandido = self.QUERY_TEMPLATE.format(prompt=texto)

        # SentenceTransformer ya aplica normalización internamente cuando
        # se especifica normalize_embeddings=True.
        embedding = self._modelo_texto.encode(
            texto_expandido,
            normalize_embeddings=True,
            convert_to_numpy=True
        )


        return embedding.tolist()

    def _cargar_imagen(self, fuente: str | Path | bytes) -> Image.Image:
        """Carga una imagen desde ruta o bytes y la convierte a RGB."""
        try:
            if isinstance(fuente, bytes):
                return Image.open(io.BytesIO(fuente)).convert("RGB")
            return Image.open(fuente).convert("RGB")
        except Exception as e:
            raise ValueError(f"No se pudo cargar la imagen: {e}")


