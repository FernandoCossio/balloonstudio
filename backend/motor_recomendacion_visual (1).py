"""---
## Sección 10: Análisis de similitud entre artículos

Genera la **matriz de similitud** entre todos los artículos del inventario. Útil para:
- Detectar artículos con imágenes demasiado similares entre sí (posibles duplicados visuales)
- Entender qué grupos de artículos el modelo percibe como visualmente relacionados
- Identificar artículos con embeddings muy distintos al resto (posibles problemas de imagen)
"""

# ==============================================================================
# SECCIÓN 10 — Matriz de similitud entre artículos
# ==============================================================================

import matplotlib.pyplot as plt
import numpy as np

# Reconstruir lista de artículos desde el store
articulos = store._articulos
n = len(articulos)

if n < 2:
    print("Se necesitan al menos 2 artículos para generar la matriz.")
else:
    # Matriz de embeddings: shape (N, 512)
    matriz = np.stack([a.embedding for a in articulos])

    # Similitud coseno entre todos los pares: shape (N, N)
    # Los vectores ya están normalizados L2, por lo que producto punto == coseno
    similitud = matriz @ matriz.T

    # Nombres cortos para los ejes
    nombres = [a.nombre[:20] for a in articulos]

    fig, ax = plt.subplots(figsize=(max(8, n * 0.7), max(6, n * 0.6)))

    im = ax.imshow(similitud, cmap="RdYlGn", vmin=0, vmax=1, aspect="auto")
    plt.colorbar(im, ax=ax, label="Similitud coseno")

    ax.set_xticks(range(n))
    ax.set_yticks(range(n))
    ax.set_xticklabels(nombres, rotation=45, ha="right", fontsize=7)
    ax.set_yticklabels(nombres, fontsize=7)

    # Anotar scores en cada celda si el inventario es pequeño
    if n <= 15:
        for i in range(n):
            for j in range(n):
                ax.text(j, i, f"{similitud[i,j]:.2f}",
                        ha="center", va="center", fontsize=6,
                        color="black" if similitud[i,j] < 0.7 else "white")

    ax.set_title("Matriz de similitud coseno entre artículos del inventario", pad=15)
    plt.tight_layout()
    plt.show()

    # Detectar pares con alta similitud (posibles duplicados visuales)
    print("\nPares de artículos con similitud > 0.85 (excluye diagonal):")
    encontrados = False
    for i in range(n):
        for j in range(i + 1, n):
            if similitud[i, j] > 0.85:
                print(f"  {articulos[i].nombre[:30]:<32} ↔  {articulos[j].nombre[:30]:<32}  score: {similitud[i,j]:.3f}")
                encontrados = True

    if not encontrados:
        print("  Ninguno — buena diversidad visual en el inventario")

